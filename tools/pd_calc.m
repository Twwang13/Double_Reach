function [PD]=pd_calc(spike_data,type)

direction = [0:5;...
    2.5:5.5,0.5,1.5;...
    3.5:5.5,0.5:2.5;...
    3,4,5,0,1,2;...
    2,3,4,5,0,1;...
    4,5,0:3
    ];
alpha = direction(type,:)/6*2*pi;  %均分一个圆,x正半轴方向为0，逆时针6等分，适用first target
% alpha = alpha(:,ones(1,3));
tmp_sum=sum(spike_data .*exp(1i*alpha));
PD=angle(tmp_sum);
end